 
public class Length {
    private int m;
    private int dm;
    private int cm;
    private int mm;

    public Length(int m, int dm, int cm, int mm) {
        this.m = m;
        this.dm = dm;
        this.cm = cm;
        this.mm = mm;
    }

    public int getM() {
        return m;
    }

    public int getDm() {
        return dm;
    }

    public int getCm() {
        return cm;
    }

    public int getMm() {
        return mm;
    }
            
}
